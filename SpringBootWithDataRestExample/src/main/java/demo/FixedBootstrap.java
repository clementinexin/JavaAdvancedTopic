@SpringBootApplication
@EnableScheduling
@EnableCaching
public class FixedBootstrap implements SchedulingConfigurer {

    public static void main(String[] args) {
        SpringApplication.run(FixedBootstrap.class, args);
    }

    @Override
    public void configureTasks(ScheduledTaskRegistrar taskRegistrar) {
        taskRegistrar.setScheduler(taskExecutor());
    }

    @Bean(destroyMethod = "shutdown")
    public Executor taskExecutor() {
        return Executors.newScheduledThreadPool(10, new ThreadFactory() {
            private final AtomicInteger threadNumber = new AtomicInteger(1);

            @Override
            public Thread newThread(Runnable r) {
                Thread t = new Thread(r, "scheduler-" + threadNumber.getAndIncrement());
                return t;
            }
        });
    }
}
